
/*
La funcion suma tiene 2 parametros de entrada y uno de salida con el 
resultado
*/

function sumar(a,b){
  var resultado = a + b;
  return resultado;
}

function restar(a,b){
  var resultado = a - b;
  return resultado;
}

function dividir(a,b){
  var resultado = a / b;
  return resultado;
}

function multiplicar(a,b){
  var resultado = a * b;
  return resultado;
}